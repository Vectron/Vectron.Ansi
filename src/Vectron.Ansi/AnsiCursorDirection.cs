namespace Vectron.Ansi;

/// <summary>
/// Possible ANSI cursor directions.
/// </summary>
public enum AnsiCursorDirection
{
    /// <summary>
    /// Move the cursor up.
    /// </summary>
    Up,

    /// <summary>
    /// Move the cursor down.
    /// </summary>
    Down,

    /// <summary>
    /// Move the cursor right.
    /// </summary>
    Right,

    /// <summary>
    /// Move the cursor left.
    /// </summary>
    Left,
}
